class Colors:
    '''ANSI color codes for terminal output'''

    HEADER     = '\033[95m'
    BLUE       = '\033[94m'
    GREEN      = '\033[92m'
    YELLOW     = '\033[93m'
    RED        = '\033[91m'
    BOLD       = '\033[1m'
    UNDERLINE  = '\033[4m'
    RESET      = '\033[0m'
    PURPLE     = '\033[35m'
    LIGHT_RED  = '\033[38;5;203m'
    DARK_GREEN = '\033[38;5;22m'
    PINK       = '\033[38;5;198m'
    GRAY       = '\033[90m'
    CYAN       = '\033[96m' 